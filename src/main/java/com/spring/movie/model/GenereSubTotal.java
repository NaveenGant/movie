package com.spring.movie.model;

public class GenereSubTotal {
	private int docTotal;
    private int shortTotal;
    private int sportTotal;
    private int newsTotal;
    private int animationTotal;
    private int romanceTotal;
    private int comedyTotal;
	public GenereSubTotal() {
		
	}
	
	public int getDocTotal() {
		return docTotal;
	}

	public void setDocTotal(int docTotal) {
		this.docTotal = docTotal;
	}

	public int getShortTotal() {
		return shortTotal;
	}
	public void setShortTotal(int shortTotal) {
		this.shortTotal = shortTotal;
	}
	public int getSportTotal() {
		return sportTotal;
	}
	public void setSportTotal(int sportTotal) {
		this.sportTotal = sportTotal;
	}
	public int getNewsTotal() {
		return newsTotal;
	}
	public void setNewsTotal(int newsTotal) {
		this.newsTotal = newsTotal;
	}
	public int getAnimationTotal() {
		return animationTotal;
	}
	public void setAnimationTotal(int animationTotal) {
		this.animationTotal = animationTotal;
	}
	public int getRomanceTotal() {
		return romanceTotal;
	}
	public void setRomanceTotal(int romanceTotal) {
		this.romanceTotal = romanceTotal;
	}
	public int getComedyTotal() {
		return comedyTotal;
	}
	public void setComedyTotal(int comedyTotal) {
		this.comedyTotal = comedyTotal;
	}
	
    
}

